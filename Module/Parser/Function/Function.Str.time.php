function function_str_time($value=null, $argumentList=array(), $parser=null){
    if(!is_array($argumentList)){
        $argumentList = (array) $argumentList;
    }
    $string = array_shift($argumentList);
    $time = array_shift($argumentList);
    if($time === null){
        $time = time();
    }
    return strtotime($string, $time);
}
